def fetch_remote(name: str, url: str) -> list:
    """下载远程 JSON."""
    req = urllib.request.Request(url, headers={"User-Agent": "Dojo/2.0"})
    data = json.loads(urllib.request.urlopen(req, timeout=30).read())
    paragraphs = []
    for chapter in data:
        key = "paragraphs" if "paragraphs" in chapter else "content"
        paras = chapter.get(key, [])
        if isinstance(paras, str): paras = [paras]
        for p in paras:
            if isinstance(p, str) and p.strip() and len(p.strip()) >= 2:
                paragraphs.append(p.strip())
    return paragraphs

def ingest():
    print("=" * 60)
    print("  道场 · 全自动经典入库")
    print("=" * 60)

    conn = sqlite3.connect(str(DB))
    cur = conn.cursor()
    cur.execute("SELECT COUNT(*) FROM parsed_classics")
    before = cur.fetchone()[0]
    print(f"\n清空前: {before} 条")
    cur.execute("DELETE FROM parsed_classics")
    conn.commit()

    stats = {}
    total = 0

    # ── 远程数据 ──
    for name, url in REMOTE_PATHS.items():
        try:
            print(f"🌐 {name}...", end=" ", flush=True)
            paras = fetch_remote(name, url)
            for p in paras:
                cur.execute("INSERT INTO parsed_classics (source_work, source_pdf, content_text, original_text, academic_annotations, modern_translation, char_count) VALUES (?,?,?,?,?,?,?)",
                    (name, name, p, p, "", "", len(p)))
            conn.commit()
            stats[name] = len(paras)
            total += len(paras)
            print(f"✅ {len(paras)} 段")
            time.sleep(0.5)  # rate limit
        except Exception as e:
            print(f"❌ {e}")

    # ── Fallback 数据 ──
    for name, chapters in FALLBACK.items():
        if name in stats: continue
        count = 0
        for chapter_title, text, translation in chapters:
            cur.execute("INSERT INTO parsed_classics (source_work, source_pdf, content_text, original_text, academic_annotations, modern_translation, char_count) VALUES (?,?,?,?,?,?,?)",
                (name, name, text, text, "", translation, len(text)))
            count += 1
        conn.commit()
        stats[name] = count
        total += count
        print(f"📦 {name}: ✅ {count} 段 (fallback)")

    conn.close()

    print(f"\n{'='*60}")
    print(f"📊 入库总计: {total} 条")
    print(f"{'='*60}")
    for name, cnt in sorted(stats.items()):
        print(f"  {name:8s}  {cnt:4d} 段")
    return total

def sync():
    print(f"\n🚀 同步到 {SERVER}...")
    remote = f"root@{SERVER}:/opt/dojo/dojo_private.db"
    ret = os.system(f"scp -i {SSH_KEY} -o StrictHostKeyChecking=no {DB} {remote}")
    if ret != 0: print("❌ SCP 失败"); return
    os.system(f'ssh -i {SSH_KEY} -o StrictHostKeyChecking=no root@{SERVER} "docker cp /opt/dojo/dojo_private.db digital-dojo:/app/data/ && docker restart digital-dojo"')
    print("✅ 服务器已同步")

if __name__ == "__main__":
    p = argparse.ArgumentParser()
    p.add_argument("--sync", action="store_true")
    args = p.parse_args()
    ingest()
    if args.sync: sync()
